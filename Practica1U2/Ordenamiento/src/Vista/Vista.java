    private void btnGenerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerarActionPerformed

        tblNumeros.setModel(new DefaultTableModel());
        CargarInterfaz();

        List<Carro> listaAutos = new ArrayList<>();
        for (int i = 0; i < Integer.parseInt(txtNumero.getText()); i++) {
            DecimalFormat df = new DecimalFormat("#.000");
            double numero = Math.round(Math.random() * 1000) / 100d;

            listaAutos.add(i, new Carro(numero));

        }
        double[] precios = new double[listaAutos.size()];

        for (int i = 0; i < precios.length; i++) {
            precios[i] = listaAutos.get(i).getNumero();
        }
        for (int i = 0; i < precios.length; i++) {
            listaAutos.get(i).setNumero(precios[i]);
        }

        Controlador.cont od = new cont();

        if (cbxSeleccion.getSelectedItem() == "Shell") {
            System.out.println("por shell");
            od.ejec(precios, "shell", (String) cbxOrden.getSelectedItem());

        } else {
            if (cbxSeleccion.getSelectedItem() == "Quicksort") {
                System.out.println("por quicksort");
                od.ejec(precios, "quicksort", (String) cbxOrden.getSelectedItem());
            }

        }

        CargarDatos(precios);
        try {
            Tiempo();
        } catch (InterruptedException ex) {
            Logger.getLogger(Vista.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_btnGenerarActionPerformed

