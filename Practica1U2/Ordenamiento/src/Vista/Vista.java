    public void Tiempo() throws InterruptedException {
        long startTime = System.nanoTime();

        /* … The code being measured starts … */
        // sleep for 5 seconds
        TimeUnit.SECONDS.sleep(5);

        /* … The code being measured ends … */
        long endTime = System.nanoTime();

        // get the difference between the two nano time valuess
        long timeElapsed = endTime - startTime;
        lblNanoSegundos.setText(String.valueOf(timeElapsed));
        lblSegundos.setText(String.valueOf(timeElapsed / 1000000));

    }

