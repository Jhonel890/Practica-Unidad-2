    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        Carro carro = new Carro(txtMarca.getText(), Integer.parseInt(txtNro_serie.getText()), txtColor.getText());
        if (filaSeleccionada != -1) {

            carroSeleccionado.setMarca(txtMarca.getText());
            carroSeleccionado.setNro_serie(Integer.parseInt(txtNro_serie.getText()));
            carroSeleccionado.setColor(txtColor.getText());
            modeloTablaCarro.removeRow(filaSeleccionada);
            modeloTablaCarro.insertRow(filaSeleccionada, crearValoresObjetoParaFila(carro));
        } else {
            Operaciones.general.getPersonas().add(carro);
            insertarFilaEnTabla(carro);
        }
        filaSeleccionada = -1;

         Ordenar();
    }//GEN-LAST:event_btnGuardarActionPerformed

