    private Object[] crearValoresObjetoParaFila(Carro producto) {
        return new Object[]{
            producto.getMarca(),
            producto.getNro_serie(),
            producto.getColor(),};
    }

    private void insertarFilaEnTabla(Carro producto) {
        modeloTablaCarro.addRow(crearValoresObjetoParaFila(producto));
    }

    private void Ordenar() {
        int valor = Integer.parseInt(txtBuscado.getText());
        Operaciones.general.guardar();
        actualizarDatos();

    }

