    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed

        Operaciones op = new Operaciones();
        int busqueda;
        busqueda = Integer.parseInt(txtBuscado.getText());

        String seleccion;
        seleccion = (String) cbxSeleccion.getSelectedItem();
        op.botonBuscar(busqueda, seleccion);
        actualizarDatos();
        op.botonBuscar(Integer.parseInt(txtBuscado.getText()), (String) cbxSeleccion.getSelectedItem());


    }//GEN-LAST:event_btnBuscarActionPerformed

